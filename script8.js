// Function declaration
function squareNumber(number) {
    console.log(number * number);
};
squareNumber(5);
// Function expression
const squareNumber2 = function (number) {
    console.log(number * number);
};
squareNumber2(4);
//  Arrow function
const squareNumber3 = (number) => {
    console.log(number * number);
};
squareNumber3(3);
